<h2>Home</h2>
You can login and register here. Try to login with different usernames like <b>user, user2, user3</b> upto <b>user15</b> 
and password is <b>password</b> for all accounts. Also, you can register a new account for demo and then login with that 
username and password also.<br>
While registering for demo have a look at the <b>Referred by</b> in this you can leave it blank or can put a value 
from 1001 - 1015 which are the values for usernames <b>user, user2, user3</b> upto <b>user15</b> or you can have a 
value which is a referral id for a user that you may have registered.